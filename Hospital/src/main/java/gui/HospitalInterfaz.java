    private void changeContent(JPanel panel){
        panel.setSize(contentP.getWidth(), contentP.getHeight());
        panel.setLocation(0,0);
        contentP.removeAll();
        contentP.add(panel, BorderLayout.CENTER);
        contentP.revalidate();
        contentP.repaint();
    }
    

    
    
   
    
    
    

    
