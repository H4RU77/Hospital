    private void AnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AnadirActionPerformed
        int res = JOptionPane.showConfirmDialog(null, addEditPanel, "Registrar Ingreso Por Emergencia", JOptionPane.OK_CANCEL_OPTION);
        if(res == 0){
            try {
                String nombre = addNombre.getText();
                int edad = Integer.parseInt(addEdad.getText());
                String razon = addRazon.getText();
                String consultorio = addConsultorio.getText();
                boolean estado = estadoCB.isSelected();
                Emergencia em = new Emergencia(consultorio, edad, estado, nombre, razon);
                EmergenciaControl.saveEmergencia(em);
                initTable();
                JOptionPane.showMessageDialog(null, "¡Expediente Añadido Exitosamente!");
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_AnadirActionPerformed

    private void buscarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBtnActionPerformed
        try {
            Emergencia em;
            List<Emergencia> ems = new ArrayList();
            if(tituloF.isSelected()){
                String nom = buscador.getText();
                ems = EmergenciaControl.getEmergenciasPac(nom);
            } else {
                Long idEmergencia = Long.parseLong(buscador.getText());
                em = EmergenciaControl.getEmergencia(idEmergencia);
                ems.add(em);
            }
            setTable(ems);
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_buscarBtnActionPerformed

