    private void estadoCBItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_estadoCBItemStateChanged
        if(!estadoCB.isSelected()){
            addConsultorio.setText("");
            addConsultorio.setEditable(true);
        } else {
            addConsultorio.setText("SALA DE EMERGENCIAS");
            addConsultorio.setEditable(false);
        }
    }//GEN-LAST:event_estadoCBItemStateChanged

    private void initTable(){
        List<Emergencia> ems = EmergenciaControl.getEmergencias();
        setTable(ems);
    }
    
    private void setTable(List<Emergencia> l){
        String[] tblH = {"ID del paciente", "Nombre del paciente", "Razón de ingreso"};
        DefaultTableModel model = new DefaultTableModel(tblH, 0);
        for (int i = 0; i < l.size(); i++){
            Emergencia emergencia =  l.get(i);
            Object[] row = {emergencia.getId(),emergencia.getNombrePaciente(),emergencia.getRazonIngreso()};
            model.addRow(row);
        }
        tablaEmergencias.setModel(model);
        tablaEmergencias.setDefaultEditor(Object.class, null);
    }
    

