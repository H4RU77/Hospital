    private void initTable(){
        List<Paciente> pacs = PacienteControl.getPacientes();
        setTable(pacs);
    }
    
    private void setTable(List<Paciente> pacs){
        String[] tblH = {"Id", "Nombre", "Telefono"};
        DefaultTableModel model = new DefaultTableModel(tblH, 0);
        for(Paciente pac: pacs){
            Object[] row = {pac.getId(), pac.getNombre(), pac.getTelefono()};
            model.addRow(row);
        }
        tablaRegistros.setModel(model);
        tablaRegistros.setDefaultEditor(Object.class, null);
    }
    
    private void setVisitas(List<Registro> regs){
        String[] tblH = {"Razon de Visita", "Fecha", "Telefono"};
        DefaultTableModel model = new DefaultTableModel(tblH, 0);
        for(Registro reg: regs){
            Object[] row = {reg.getRazonVisita(), reg.getFecha()};
            model.addRow(row);
        }
        tablaVisitas.setModel(model);
        tablaVisitas.setDefaultEditor(Object.class, null);
    }

