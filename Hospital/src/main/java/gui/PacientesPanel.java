    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        int res = JOptionPane.showConfirmDialog(null, addEditPanel, "Registrar Paciente", JOptionPane.OK_CANCEL_OPTION);
        if(res == 0){
            try {
                razonVL.setVisible(true);
                addRazon.setVisible(true);
                String nom = addNom.getText();
                Paciente pac = PacienteControl.getPacienteNom(nom);
                if (pac == null){
                    int edad = Integer.parseInt(addEdad.getText());
                    String tel = addTel.getText();
                    pac = new Paciente(nom, edad, tel);
                    PacienteControl.savePaciente(pac);
                }
                Registro reg = new Registro(pac, addRazon.getText());
                Registro r = PacienteControl.saveRegistro(reg);
                initTable();
                JOptionPane.showMessageDialog(null, "¬°Registro A√±adido Exitosamente!\n"
                        + "N√∫mero de Identificaci√≥n del Paciente: "+r.getIdPaciente().getId());
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void listadoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listadoBtnActionPerformed
        List<Expediente> exps = ExpControl.getExpedientes();
        List<Paciente> pacs = new ArrayList();
        String nomMed = ((RecepcionistaPersonal) mainFrame.getCurrUser()).getMedicoAsignado();
        for(Expediente ex: exps){
            if(ex.getMedico().getNombre().equals(nomMed)){
                pacs.add(ex.getPaciente());
            }
        }
        ContentChange.generarListado(pacs, nomMed);
    }//GEN-LAST:event_listadoBtnActionPerformed

    private void consultBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultBtnActionPerformed
        try {
            int row = tablaRegistros.getSelectedRow();
            Long idPaciente = (Long) tablaRegistros.getValueAt(row, 0);
            Paciente pac = PacienteControl.getPacienteById(idPaciente);
            List<Registro> regs = PacienteControl.getRegistros(idPaciente);
            consulNom.setText(pac.getNombre());
            consulEdad.setText(String.valueOf(pac.getEdad()));
            consulTel.setText(pac.getTelefono());
            setVisitas(regs);
            JOptionPane.showMessageDialog(null, consultarPanel);
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_consultBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        try {
            int row = tablaRegistros.getSelectedRow();
            Long idPaciente = (Long) tablaRegistros.getValueAt(row, 0);
            Paciente pac = PacienteControl.getPacienteById(idPaciente);
            addNom.setText(pac.getNombre());
            addEdad.setText(String.valueOf(pac.getEdad()));
            addTel.setText(pac.getTelefono());
            razonVL.setVisible(false);
            addRazon.setVisible(false);
            int res = JOptionPane.showConfirmDialog(null, addEditPanel, "Actualizar Datos Paciente", JOptionPane.OK_CANCEL_OPTION);
            if(res == 0){
                try {
                    String nom = addNom.getText();
                    int edad = Integer.parseInt(addEdad.getText());
                    String tel = addTel.getText();
                    pac.setNombre(nom);
                    pac.setEdad(edad);
                    pac.setTelefono(tel);
                    PacienteControl.updatePaciente(pac);
                    initTable();
                    JOptionPane.showMessageDialog(null, "¬°Paciente Actualizado Exitosamente!");
                } catch(Exception e){
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_editBtnActionPerformed

